const isOwnerOf = async (signer, contractAddress, tokenId, chain) => {

  // check if token exists 

  // check if is owner of 
  const polygonEndpoint = "https://polygon-mainnet.infura.io/v3/"
  const ethereumEndpoint = "https://mainnet.infura.io/v3/"
  let endpoint;

  if (chain === "Ethereum") {
    endpoint = ethereumEndpoint + "f744238ecde747519e5973389b1240e0"
  }

  if (chain === "Polygon") {
    endpoint = polygonEndpoint + "f744238ecde747519e5973389b1240e0"
  }

  console.log("OWNER OF DATA", signer, contractAddress, tokenId, chain)

  const provider = new ethers.providers.JsonRpcProvider(
    endpoint
  );

  const contractWithSigner = new ethers.Contract(
    contractAddress,
    ERC721_ABI,
    provider
  );

  try {
    const ownerOf = await contractWithSigner.ownerOf(tokenId);
    console.log("OWNER", ownerOf)
    if (ownerOf.toLowerCase() === signer.toLowerCase() ) {
      return true;
    } else {
      throw new Error(`${signer} is not owner of token address ${contractAddress} and token id ${tokenId}`);
    }
  } catch (error) {
    console.log("OWNEROF_ERROR", error)
    return false;
  }

}


